package com.company;

public class Person {
    private String name;
    private String surname;
    private int DNI;
    private int age;
    public Person (){}
    public Person(String name,String surname,int DNI,int age)
    {
        this.DNI=DNI;
        this.name=name;
        this.surname=surname;
        this.age=age;
    }
}
